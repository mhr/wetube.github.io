        <div id="q6" class="box">
      		<a href="#q6">
        		Why did you choose to go this route, with bitcoin-like protocols that aim to decentralize the internet?
        	</a>
      		<div class="answer">
        		<!-- ANSWER -->
        		The very reason that bitcoin chose to decentralize the recording of transactions. The problem with 
        		centralisation is that it gives to much power to those who hold the data. In the case of bitcoin,
        		they wish to grant the use of currency back to their user - where governments can not increase
        		the currency in circulation leading to inflation.
        		
        		Now, with our project, we wish to do the same thing with the internet - we wish to grant internet
        		users back their power. By removing the ISP, we remove the problems of filtering, bandwidth 
        		throttling etc., and to keep anonyimity (we plan to link into the TOR system). By distributing 
        		the system, we ensure that no one can have complete power over the system - each node needs another
        		node, and so can not exist soley and thus have little power over the user.
        	</div>
